/* Chrome Dev Tools: Son una serie de herramientas del navegador que permiten ver el comportamiento 
 de nuestro código y debuggear. Se divide en:
 - Elements: sirve para ver o modificar DOM y CSS.
 - Console: Para ver mensajes que se van a consola.
 - Sources: Tiene 3 partes: 
            - File Navigator.
            - Code Editor.
            - JavaScript debugging.
 - Network: Se usa para checar network activity de una página web.
*/

//////////////////////////////////////////////////////////////////////////////////////////////////////
/* En el source panel, se puede hacer debugging:
   - Para debuggear, se utilizan breakpoints para empezar a observar el comportamiento del código 
     desde un punto en específico.
   - Stepping through code: se pueden usar opciones como step into, step over next function, etc... ésto
                            es de gran ayuda para visualizar línea por línea el código.
   - Checking variables: En scope se verán algunas variables que se podrá ver su cambio a lo largo del programa,
                         también se tiene 'watch' dónde uno puede declarar variables específicas y ver su
                         comportamiento a lo largo del flujo del programa.
*/